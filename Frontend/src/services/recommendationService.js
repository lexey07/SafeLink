export function getRecommendation(riskScore) {
    if (riskScore === undefined || riskScore === null) {
        return "";
    }

    if (riskScore === 0) {
        return "Сайт выглядит безопасным. Тем не менее всегда проверяйте адрес сайта перед вводом паролей и банковских данных.";
    }

    if (riskScore <= 35) {
        return "Обнаружены незначительные признаки риска. Рекомендуется внимательно проверить адрес сайта перед вводом личных данных.";
    }

    if (riskScore <= 65) {
        return "Обнаружено несколько подозрительных признаков. Не рекомендуется вводить пароли, коды подтверждения или банковские данные без дополнительной проверки.";
    }

    return "Высока вероятность мошенничества или фишинга. Не вводите личные данные и не переходите по ссылке без необходимости.";
}